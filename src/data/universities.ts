// Helper function to search universities
export function searchUniversities(query: string, limit: number = 8): string[] {
  if (!query || query.length < 2) return [];
  
  const lowerQuery = query.toLowerCase().trim();
  
  // Exact matches first
  const exactMatches = universities.filter(u => 
    u.toLowerCase() === lowerQuery
  );
  
  // Starts with matches
  const startsWithMatches = universities.filter(u => 
    u.toLowerCase().startsWith(lowerQuery) && 
    !exactMatches.includes(u)
  );
  
  // Contains matches
  const containsMatches = universities.filter(u => 
    u.toLowerCase().includes(lowerQuery) && 
    !exactMatches.includes(u) && 
    !startsWithMatches.includes(u)
  );
  
  // Combine and limit results
  return [...exactMatches, ...startsWithMatches, ...containsMatches].slice(0, limit);
}

