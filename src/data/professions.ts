// Helper function to search professions
export function searchProfessions(query: string, limit: number = 5): string[] {
  if (!query || query.length < 2) return [];
  
  const lowerQuery = query.toLowerCase().trim();
  
  // Exact matches first
  const exactMatches = professions.filter(p => 
    p.toLowerCase() === lowerQuery
  );
  
  // Starts with matches
  const startsWithMatches = professions.filter(p => 
    p.toLowerCase().startsWith(lowerQuery) && 
    !exactMatches.includes(p)
  );
  
  // Contains matches
  const containsMatches = professions.filter(p => 
    p.toLowerCase().includes(lowerQuery) && 
    !exactMatches.includes(p) && 
    !startsWithMatches.includes(p)
  );
  
  // Combine and limit results
  return [...exactMatches, ...startsWithMatches, ...containsMatches].slice(0, limit);
}

